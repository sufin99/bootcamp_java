public class VariablesTipos {

	public static void main(String[] args) {
		int a = 5;
		double b = 5.1;
		boolean c = true;
		char d = 'a';
		String e = "Hola, Mundo";

		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
	}
}
